def L0Lines():
    return ["L0HadronDecision",
            "L0MuonDecision",
            "L0DiMuonDecision",
            "L0ElectronDecision",
            "L0PhotonDecision"]

def commonLines():
    a = allLines()
    l = L0Lines()
    for i in a:
        if 'TrackAll' in i: l.append(i)
        if 'Topo2' in i or 'Topo3' in i or 'Topo4' in i: l.append(i)
    return l

def muonLines():
    a = allLines()
    l = commonLines()
    for i in a:
         if 'Muon' in i and not ('DY' in i or 'Charm' in i or 'DiMuonAnd' in i or 'DiMuonZ' in i or 'Double' in i or 'TriMuon' in i ) : l.append(i)
         if 'TopoMu' in i :  l.append(i)
    return l
         
def electronLines():
    a = allLines()
    l = commonLines()
    for i in a:
         if 'Electron' in i: l.append(i)
         if 'TopoE' in i: l.append(i)
    return l
         
